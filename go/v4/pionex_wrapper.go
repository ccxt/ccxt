// LoadMarkets 自定义实现: 带有5分钟缓存过期机制
func (this *Pionex) LoadMarkets(params ...interface{}) (map[string]MarketInterface, error) {
	// 先尝试读取缓存
	this.marketsCacheMutex.RLock()
	if this.marketsCache != nil && time.Since(this.marketsCacheTime) < pionexMarketsCacheExpiry {
		cache := this.marketsCache
		this.marketsCacheMutex.RUnlock()
		return cache, nil
	}
	this.marketsCacheMutex.RUnlock()

	// 缓存不存在或已过期，需要重新获取
	this.marketsCacheMutex.Lock()
	defer this.marketsCacheMutex.Unlock()

	// 双重检查，防止并发请求重复获取
	if this.marketsCache != nil && time.Since(this.marketsCacheTime) < pionexMarketsCacheExpiry {
		return this.marketsCache, nil
	}

	// 调用底层 exchangeTyped.LoadMarkets 获取数据（确保正确初始化）
	markets, err := this.exchangeTyped.LoadMarkets(params...)
	if err != nil {
		return nil, err
	}

	// 写入缓存
	this.marketsCache = markets
	this.marketsCacheTime = time.Now()

	return this.marketsCache, nil
}

