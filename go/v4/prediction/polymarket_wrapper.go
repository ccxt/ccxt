func (this *Polymarket) FetchOrders(options ...FetchOrdersOptions) ([]ccxt.PredictionOrder, error) {

    opts := FetchOrdersOptionsStruct{}

    for _, opt := range options {
        opt(&opts)
    }

    var outcome any = nil
    if opts.Outcome != nil {
        outcome = *opts.Outcome
    }

    var since any = nil
    if opts.Since != nil {
        since = *opts.Since
    }

    var limit any = nil
    if opts.Limit != nil {
        limit = *opts.Limit
    }

    var params any = nil
    if opts.Params != nil {
        params = *opts.Params
    }
    res := <- this.Core.FetchOrders(outcome, since, limit, params)
    if ccxt.IsError(res) {
        return nil, ccxt.CreateReturnError(res)
    }
    return ccxt.NewPredictionOrderArray(res), nil
}
