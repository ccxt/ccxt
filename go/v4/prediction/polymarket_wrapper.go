func (this *Polymarket) WatchPositions(options ...WatchPositionsOptions) ([]ccxt.PredictionPosition, error) {

    opts := WatchPositionsOptionsStruct{}

    for _, opt := range options {
        opt(&opts)
    }

    var outcomes any = nil
    if opts.Outcomes != nil {
        outcomes = *opts.Outcomes
    }

    var since any = nil
    if opts.Since != nil {
        since = *opts.Since
    }

    var limit any = nil
    if opts.Limit != nil {
        limit = *opts.Limit
    }

    var params any = nil
    if opts.Params != nil {
        params = *opts.Params
    }
    res := <- this.Core.WatchPositions(outcomes, since, limit, params)
    if ccxt.IsError(res) {
        return nil, ccxt.CreateReturnError(res)
    }
    return ccxt.NewPredictionPositionArray(res), nil
}
