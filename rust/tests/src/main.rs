// CCXT Rust – test runner entry point.
//
// Hand-written. Mirrors `go/tests/main.go` and `cs/tests/tests.csproj`:
//   ti-rust [exchange] [symbol] [method] [--baseTests] [--requestTests] [--responseTests] [--ws]
//
// What works today:
//   --baseTests      runs Precise / Value / safeXxx assertions against the
//                    hand-written base crate (offline, no exchanges needed)
//   --requestTests   loads ts/src/test/static/request/<exchange>.json,
//                    validates schema, and dispatches into the exchange
//                    registry (empty until the transpiler runs)
//   --responseTests  same shape for response fixtures
//
// When `rust/ccxt/src/exchanges/` is populated by the transpiler, the
// exchange registry below will be extended (it currently returns
// `Skipped` for every exchange — non-fatal so CI passes).

#![allow(dead_code)] // fixture / registry fields are read by the (transpiler-extended) dispatcher

use std::env;
use std::fs;
use std::path::PathBuf;
use std::process::ExitCode;

mod assertions;
mod base_tests;
mod fixtures;
mod registry;
pub mod tests_support; // public so transpiled base/ tests can reach it
// Transpiled base tests (generated by `tsx build/rustTranspiler.ts --tests`).
// Currently behind a feature flag — the transpiled output references
// helpers we haven't fully shimmed yet, so including unconditionally
// breaks the build. Enable with `cargo build --features transpiled-tests`
// to iterate on the remaining ~900 errors.
#[cfg(feature = "transpiled-tests")]
#[path = "../base/mod.rs"]
mod base_transpiled;
// `tests.rs` is generated from `ts/src/test/tests.ts` by
// `tsx build/rustTranspiler.ts --tests` (parallels go/tests/base/tests.go).
// It doesn't compile yet — the transpiled `testMainClass` references
// runtime helpers (dynamic method dispatch by name, file I/O,
// exception-instance checks, `Promise.all`, …) we haven't wired into
// the Rust port. Once those land the comparators below can be deleted
// in favour of the transpiled `assertStaticRequestOutput` etc.
// mod tests;

#[derive(Default, Debug)]
struct Args {
    base_tests:     bool,
    request_tests:  bool,
    response_tests: bool,
    ws:             bool,
    sync:           bool,
    verbose:        bool,
    exchange:       Option<String>,
    symbol:         Option<String>,
    method:         Option<String>,
}

fn parse_args() -> Args {
    let mut a = Args::default();
    let mut positional: Vec<String> = Vec::new();
    for arg in env::args().skip(1) {
        match arg.as_str() {
            "--baseTests"     => a.base_tests     = true,
            "--requestTests"  | "--request"  => a.request_tests  = true,
            "--responseTests" | "--response" => a.response_tests = true,
            "--ws"            => a.ws             = true,
            "--sync"          => a.sync           = true,
            "--verbose" | "-v" => a.verbose       = true,
            s if s.starts_with("--") => { /* ignore unknown flags for compat */ }
            _ => positional.push(arg),
        }
    }
    a.exchange = positional.get(0).cloned();
    a.symbol   = positional.get(1).cloned();
    a.method   = positional.get(2).cloned();
    a
}

fn repo_root() -> PathBuf {
    // tests crate lives at <repo>/rust/tests — walk up two dirs.
    let here = env!("CARGO_MANIFEST_DIR");
    PathBuf::from(here).join("..").join("..")
}

#[tokio::main]
async fn main() -> ExitCode {
    let args = parse_args();

    if args.base_tests {
        // Hand-written suite first (Precise, Value, safe_*, deep_extend).
        if let Err(e) = base_tests::run(args.ws) {
            eprintln!("Hand-written base tests failed: {e}");
            return ExitCode::FAILURE;
        }
        println!("Hand-written base {} tests passed!", if args.ws { "WS" } else { "REST" });
        // Transpiled suite (test.safeMethods.rs, test.precise.rs, …)
        // generated from ts/src/test/base/. Behind a feature flag
        // until the remaining helper shims land — see main.rs.
        #[cfg(feature = "transpiled-tests")]
        {
            println!("Running transpiled base tests:");
            if let Err(e) = base_transpiled::run_all() {
                eprintln!("Transpiled base tests failed: {e}");
                return ExitCode::FAILURE;
            }
            println!("Transpiled base tests passed!");
        }
        return ExitCode::SUCCESS;
    }

    if args.request_tests || args.response_tests {
        let kind = if args.request_tests { "request" } else { "response" };
        let root = repo_root();
        let dir  = root.join("ts/src/test/static").join(kind);

        let targets: Vec<String> = match &args.exchange {
            Some(name) => vec![name.clone()],
            None => match list_fixtures(&dir) {
                Ok(v)  => v,
                Err(e) => { eprintln!("could not list {dir:?}: {e}"); return ExitCode::FAILURE; }
            },
        };

        let mut failed   = 0usize;
        let mut skipped  = 0usize;
        let mut ok       = 0usize;

        for ex in &targets {
            let path = dir.join(format!("{ex}.json"));
            if !path.exists() {
                if args.verbose { eprintln!("[skip] {ex}: no fixture at {path:?}"); }
                skipped += 1;
                continue;
            }
            match fixtures::load(&path) {
                Err(e) => {
                    eprintln!("[fail] {ex}: fixture parse — {e}");
                    failed += 1;
                }
                Ok(fixture) => {
                    if !registry::has(ex) {
                        if args.verbose { println!("[skip] {ex}: not yet generated"); }
                        skipped += 1;
                        continue;
                    }
                    let (pass, fail) = run_request_fixture(
                        &fixture,
                        args.method.as_deref(),
                        args.verbose,
                    ).await;
                    if fail == 0 {
                        println!("[ok]   {ex}: {pass} pass, 0 fail");
                        ok += 1;
                    } else {
                        println!("[fail] {ex}: {pass} pass, {fail} fail");
                        failed += 1;
                    }
                }
            }
        }

        println!(
            "{} tests: {} ok, {} skipped, {} failed (of {} target{})",
            kind,
            ok,
            skipped,
            failed,
            targets.len(),
            if targets.len() == 1 { "" } else { "s" },
        );
        return if failed == 0 { ExitCode::SUCCESS } else { ExitCode::FAILURE };
    }

    // Default: print usage.
    eprintln!(
        "usage: ti-rust [<exchange> [<symbol> [<method>]]] \
         [--baseTests] [--requestTests] [--responseTests] [--ws] [--verbose]"
    );
    ExitCode::FAILURE
}

/// Runs every method/case in a request fixture and returns (pass, fail).
async fn run_request_fixture(
    fixture: &fixtures::Fixture,
    only_test: Option<&str>,
    verbose: bool,
) -> (usize, usize) {
    use ccxt::Value;
    let mut pass = 0usize;
    let mut fail = 0usize;
    let options = serde_to_value(&fixture.options);
    let global_options = options.clone();

    // Sorted iteration for stable output.
    let mut method_names: Vec<&String> = fixture.methods.keys().collect();
    method_names.sort();

    for method in method_names {
        let cases = &fixture.methods[method];
        for case in cases {
            // `description` lets users filter by --method <description>.
            let description = case.get("description").and_then(|v| v.as_str()).unwrap_or("");
            if let Some(name) = only_test {
                if name != description && name != method { continue; }
            }
            // Per-case disable flag.
            if case.get("disabled").and_then(|v| v.as_bool()).unwrap_or(false) { continue; }

            let input: Vec<Value> = case.get("input")
                .and_then(|v| v.as_array())
                .map(|a| a.iter().map(serde_to_value).collect())
                .unwrap_or_default();

            // Per-case options merge.
            let case_opts = match case.get("options") {
                Some(v) => serde_to_value(v),
                None    => Value::Null,
            };
            let merged_opts = merge_options(&global_options, &case_opts);

            let captured = registry::dispatch(
                &fixture.exchange,
                method,
                input,
                &merged_opts,
            ).await;

            let expected_url = case.get("url").and_then(|v| v.as_str()).unwrap_or("");
            let actual_url   = captured.url.as_str();
            let url_ok = compare_urls(actual_url, expected_url, &fixture.skip_keys);

            // Body comparison: only fail when the fixture provides one.
            let body_ok = match case.get("output") {
                Some(serde_json::Value::Null) | None => true,
                Some(expected) => compare_body(&captured.body, expected, &fixture.skip_keys, fixture.output_type.as_deref()),
            };

            if url_ok && body_ok {
                if verbose { println!("  ✓ {method}/{description}"); }
                pass += 1;
            } else {
                println!("  ✗ {method}/{description}");
                if !url_ok {
                    println!("    expected url: {expected_url}");
                    println!("    actual   url: {actual_url}");
                }
                if !body_ok {
                    let expected_body = case.get("output").and_then(|v| v.as_str()).unwrap_or("<non-string>");
                    let actual_body   = captured.body.as_deref().unwrap_or("");
                    println!("    body mismatch");
                    println!("      expected: {expected_body}");
                    println!("      actual:   {actual_body}");
                }
                fail += 1;
            }
        }
    }
    (pass, fail)
}

/// URL equality modulo query-string key order, ignored keys, and host.
/// Mirrors `assertStaticRequestOutput` in ts/src/test/tests.ts: when the
/// full URL doesn't match, strip the host and compare only the path +
/// query.
fn compare_urls(actual: &str, expected: &str, skip_keys: &[String]) -> bool {
    if actual == expected { return true; }
    let (au, aq) = split_url(actual);
    let (eu, eq) = split_url(expected);
    let path_match = au == eu || strip_hostname(au) == strip_hostname(eu);
    if !path_match { return false; }
    compare_qs(aq, eq, skip_keys)
}

/// Mirrors `assertNewAndStoredOutput` for dicts: iterates the expected
/// keys, skips skip_keys, and requires actual to have the same key
/// (with equal value) plus matching total key count. A key present in
/// actual but not expected is allowed as long as length matches (this
/// is how the TS runner accepts e.g. `clientAlgoId` vs `newClientOrderId`
/// when `newClientOrderId` is in skip_keys).
fn compare_qs(actual_qs: &str, expected_qs: &str, skip_keys: &[String]) -> bool {
    let a = parse_qs_raw(actual_qs);
    let e = parse_qs_raw(expected_qs);
    if a.len() != e.len() { return false; }
    for (k, v) in &e {
        if skip_keys.contains(k) { continue; }
        match a.get(k) {
            None => return false,
            Some(av) => {
                if av == v { continue; }
                // Both may be JSON arrays/objects — parse and compare
                // recursively so nested `newClientOrderId` etc. get
                // skip_keys treatment too.
                if let (Ok(aj), Ok(ej)) = (
                    serde_json::from_str::<serde_json::Value>(av),
                    serde_json::from_str::<serde_json::Value>(v),
                ) {
                    if json_eq_skip(&aj, &ej, skip_keys) { continue; }
                }
                return false;
            }
        }
    }
    true
}

fn parse_qs_raw(qs: &str) -> std::collections::BTreeMap<String, String> {
    let mut m = std::collections::BTreeMap::new();
    if qs.is_empty() { return m; }
    for pair in qs.split('&') {
        let mut it = pair.splitn(2, '=');
        let k = url_decode(it.next().unwrap_or(""));
        let v = url_decode(it.next().unwrap_or(""));
        m.insert(k, v);
    }
    m
}

/// `https://api.binance.com/foo/bar` → `/foo/bar`.
fn strip_hostname(u: &str) -> &str {
    if let Some(rest) = u.strip_prefix("https://").or_else(|| u.strip_prefix("http://")) {
        match rest.find('/') {
            Some(i) => &rest[i..],
            None    => "",
        }
    } else {
        u
    }
}

fn split_url(u: &str) -> (&str, &str) {
    match u.find('?') {
        Some(i) => (&u[..i], &u[i + 1..]),
        None    => (u, ""),
    }
}

fn parse_qs(qs: &str, skip: &[String]) -> std::collections::BTreeMap<String, String> {
    let mut m = std::collections::BTreeMap::new();
    if qs.is_empty() { return m; }
    for pair in qs.split('&') {
        let mut it = pair.splitn(2, '=');
        let k = url_decode(it.next().unwrap_or(""));
        let v = url_decode(it.next().unwrap_or(""));
        if skip.contains(&k) { continue; }
        m.insert(k, v);
    }
    m
}

/// Decode a percent-encoded URL segment.
fn url_decode(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    let bytes = s.as_bytes();
    let mut i = 0;
    while i < bytes.len() {
        if bytes[i] == b'%' && i + 2 < bytes.len() {
            let hi = (bytes[i + 1] as char).to_digit(16);
            let lo = (bytes[i + 2] as char).to_digit(16);
            if let (Some(h), Some(l)) = (hi, lo) {
                out.push(((h * 16 + l) as u8) as char);
                i += 3;
                continue;
            }
        }
        if bytes[i] == b'+' { out.push(' '); } else { out.push(bytes[i] as char); }
        i += 1;
    }
    out
}

/// Body comparison. Output may be a string (raw), a number (treated as
/// string), or a structured JSON object.
fn compare_body(
    actual: &Option<String>,
    expected: &serde_json::Value,
    skip_keys: &[String],
    output_type: Option<&str>,
) -> bool {
    let a = actual.as_deref().unwrap_or("");
    match expected {
        serde_json::Value::Null         => true,
        serde_json::Value::String(s)    => match output_type {
            Some("urlencoded") => compare_qs(a, s, skip_keys),
            _ => a == s,
        }
        serde_json::Value::Object(_)    => {
            let a_json: serde_json::Value = match serde_json::from_str(a) {
                Ok(j) => j,
                Err(_) => return false,
            };
            json_eq_skip(&a_json, expected, skip_keys)
        }
        _ => a == expected.to_string(),
    }
}

/// JSON equality with `skip_keys` masked at any depth.
fn json_eq_skip(a: &serde_json::Value, b: &serde_json::Value, skip: &[String]) -> bool {
    match (a, b) {
        (serde_json::Value::Object(am), serde_json::Value::Object(bm)) => {
            let ak: std::collections::BTreeSet<_> = am.keys().filter(|k| !skip.contains(k)).collect();
            let bk: std::collections::BTreeSet<_> = bm.keys().filter(|k| !skip.contains(k)).collect();
            if ak != bk { return false; }
            ak.iter().all(|k| json_eq_skip(&am[*k], &bm[*k], skip))
        }
        (serde_json::Value::Array(av), serde_json::Value::Array(bv)) => {
            av.len() == bv.len() && av.iter().zip(bv.iter()).all(|(x, y)| json_eq_skip(x, y, skip))
        }
        _ => a == b,
    }
}

fn merge_options(global: &ccxt::Value, case: &ccxt::Value) -> ccxt::Value {
    use ccxt::Value;
    match (global, case) {
        (Value::Map(g), Value::Map(c)) => {
            let mut m = g.clone();
            for (k, v) in c { m.insert(k.clone(), v.clone()); }
            Value::Map(m)
        }
        (Value::Null, c) => c.clone(),
        (g, Value::Null) => g.clone(),
        (_, c) => c.clone(),
    }
}

fn serde_to_value(j: &serde_json::Value) -> ccxt::Value {
    use ccxt::Value;
    match j {
        serde_json::Value::Null         => Value::Null,
        serde_json::Value::Bool(b)      => Value::Bool(*b),
        serde_json::Value::Number(n)    => {
            if let Some(i) = n.as_i64() { Value::Int(i) }
            else if let Some(f) = n.as_f64() { Value::Float(f) }
            else { Value::Null }
        }
        serde_json::Value::String(s)    => Value::Str(s.clone()),
        serde_json::Value::Array(a)     => Value::Array(a.iter().map(serde_to_value).collect()),
        serde_json::Value::Object(o)    => {
            let mut m = std::collections::HashMap::new();
            for (k, v) in o { m.insert(k.clone(), serde_to_value(v)); }
            Value::Map(m)
        }
    }
}

fn list_fixtures(dir: &PathBuf) -> std::io::Result<Vec<String>> {
    let mut v: Vec<String> = fs::read_dir(dir)?
        .filter_map(|e| e.ok())
        .filter_map(|e| {
            let p = e.path();
            if p.extension().and_then(|s| s.to_str()) == Some("json") {
                p.file_stem().and_then(|s| s.to_str()).map(str::to_owned)
            } else {
                None
            }
        })
        .collect();
    v.sort();
    Ok(v)
}
