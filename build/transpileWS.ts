    createPythonClassHeader (ccxtImports, bodyAsString) {
        const imports = [
            ... ccxtImports,
        ]
        const arrayCacheClasses = bodyAsString.match (/\bArrayCache(?:[A-Z][A-Za-z]+)?\b/g)
        if (arrayCacheClasses) {
            const uniqueArrayCacheClasses = unique (arrayCacheClasses).sort ()
            const arrayCacheImport = 'from ccxt.async_support.base.ws.cache import ' + uniqueArrayCacheClasses.join (', ')
            imports.push (arrayCacheImport)
        }
        const orderBookClasses = bodyAsString.match(/\s(Asks|Bids)\(.*\)/g)
        if (orderBookClasses) {
            const uniqueOrderBookClasses = unique (orderBookClasses.map(match => match.replace(/\(.*\)/, '').trim())).sort ()
            const orderBookSideImport = 'from ccxt.async_support.base.ws.order_book_side import ' + uniqueOrderBookClasses.join (', ')
            imports.push (orderBookSideImport)
        }
        return [
            "# -*- coding: utf-8 -*-",
            "",
            "# PLEASE DO NOT EDIT THIS FILE, IT IS GENERATED AND WILL BE OVERWRITTEN:",
            "# https://github.com/ccxt/ccxt/blob/master/CONTRIBUTING.md#how-to-contribute-code",
            "",
            ... imports,
        ]
    }

    createPHPClassDeclaration (className, baseClass) {
        let lines: string[] = []
        // prediction is async-only and flattened: the async REST class is \ccxt\prediction\<id>
        const asyncNamespace = this.isPrediction ? '\\ccxt\\prediction\\' : '\\ccxt\\async\\'
        const proNamespace = this.isPrediction ? '\\ccxt\\prediction\\pro\\' : '\\ccxt\\pro\\'
        if (baseClass.indexOf ('Rest') >= 0) {
            //     lines = lines.concat ([
            //         '',
            //         // '    use ClientTrait;'
            //     ])
            lines.push('class ' + className + ' extends ' + asyncNamespace +  baseClass.replace ('Rest', '') + ' {')
        } else {
            lines.push('class ' + className + ' extends ' + proNamespace +  baseClass + ' {')
        }
        return lines.join ("\n")
    }

    createPHPClassHeader (className, baseClass, bodyAsString) {
        const header = [
            "<?php",
            "",
            this.isPrediction ? "namespace ccxt\\prediction\\pro;" : "namespace ccxt\\pro;",
            "",
            "// PLEASE DO NOT EDIT THIS FILE, IT IS GENERATED AND WILL BE OVERWRITTEN:",
            "// https://github.com/ccxt/ccxt/blob/master/CONTRIBUTING.md#how-to-contribute-code",
            "",
            "use Exception; // a common import",
        ]
        if (this.isPrediction) {
            // the ws infrastructure classes live in the ccxt\pro namespace — classes
            // outside of it need explicit imports for any of them that they reference
            const proInfraClasses = [
                'ArrayCache', 'ArrayCacheBySymbolById', 'ArrayCacheBySymbolBySide', 'ArrayCacheByTimestamp',
                'OrderBook', 'IndexedOrderBook', 'CountedOrderBook',
                'Asks', 'Bids', 'CountedAsks', 'CountedBids', 'IndexedAsks', 'IndexedBids',
            ]
            for (const infraClass of proInfraClasses) {
                const regex = new RegExp ('\\b(?:new\\s+|instanceof\\s+)' + infraClass + '\\b')
                if (bodyAsString.match (regex)) {
                    header.push ('use ccxt\\pro\\' + infraClass + ';')
                }
            }
        }
        return header
    }

