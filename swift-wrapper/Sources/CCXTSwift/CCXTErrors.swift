import Foundation

class BaseError: Error, CustomStringConvertible, LocalizedError {
    let message: String
    var name: String { "BaseError" }

    required init(_ message: String) {
        self.message = message
    }

    var description: String {
        return "\(name): \(message)"
    }

    public var errorDescription: String? { 
        return "\(name): \(message)"
    }
}

